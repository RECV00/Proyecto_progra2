public void fillTable(ArrayList <Passenger> list) {
		
		for(Passenger p : list) {
			dtmTPassenger.addRow(new Object[] {p.getPassport(),p.getName(),p.getLastName(),
					p.getBirthdate(),p.getGmail(),p.getPhone()});
		}
		setPassenger(dtmTPassenger);
	}

